#Использование модуля черепашки
import turtle
t = turtle.Pen()
t.forward(50)
t.left(90)
t.forward(50)
t.left(90)
t.forward(50)
t.left(90)
t.forward(50)
t.left(90)
#Чтобы очистить холст, введите команду reset. При этом с холста
#исчезнет все нарисованное, а черепашка вернется в начальную позицию.
t.reset()
#Холст также очистится, но черепашка останется на прежнем месте.
t.clear()
t.backward(100)
t.up()
t.right(90)
t.forward(20)
t.left(90)
t.down()
t.forward(100)
